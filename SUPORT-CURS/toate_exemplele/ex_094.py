A = [
    [
        ["a", 55, 146],
        ["b", 34, 124],
        ["c", 96, 564],
        [100, 12, "d"],
    ],
    [
        ["e", 88, 146],
        ["f", 34, 124],
        ["g", 96, 564],
        [100, 12, "h"],
    ],
    [
        ["i", 88, 146],
        ["j", 34, 124],
        ["k", 96, 564],
        [100, 12, "k"],
    ],
    [
        ["m", 88, 146],
        ["n", 34, 124],
        ["o", 96, 564],
        [100, 12, "p"],
    ],
    [
        ["q", 88, 146],
        ["r", 34, 124],
        ["s", 96, 564],
        [100, 12, "t"],
    ]
]

t = ""
s = len(A)       # matrices
m = len(A[0])    # rows
n = len(A[0][0]) # columns
q = s * m * n

for v in range(q):
    k = v % (m * n)
    j = k % n
    i = k // n
    d = v // (m * n)
    t += f"{v} A[{d}][{i}][{j}] = {A[d][i][j]}\n"

print(t)